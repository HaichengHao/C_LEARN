//**短路现象**
//
//- &&：a && b
//  - 当 a 为假(或0)时，因为a && b 结果必定为 0，所以不再执行表达式 b
//  - 当 a 为真(非0)时，因为a && b 结果不确定，所以会继续求解表达式b
#include <stdio.h>
int main(){
    int a1,b1;
    a1 = 10;
    b1 = 10;
    if (a1 > 5 && b1++ >0){
        printf("yes\n");
    } else{
        printf("no\n");
    }
    printf("b1 = %d\n",b1);

    int a2,b2;
    a2 = 10;
    b2 = 10;
    if (a2 > 20 && b2++ >0){
        printf("yes\n");
    } else{
        printf("no\n");
    }
    printf("b2 = %d\n",b2);

    return 0;
}
