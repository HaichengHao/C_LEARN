/*不同的进制只是整数的书写方法不同，不会对整数的实际存储方式产生影响。不同进制可以混合使用，比如 10 + 015 + 0x20 是一个合法的表达式。

printf() 的进制相关占位符如下：
    - %d ：十进制整数。
    - %o ：八进制整数。
    - %x ：十六进制整数。
    - %#o ：显示前缀 0 的八进制整数。
    - %#x ：显示前缀 0x 的十六进制整数。
    - %#X ：显示前缀 0X 的十六进制整数。*/

