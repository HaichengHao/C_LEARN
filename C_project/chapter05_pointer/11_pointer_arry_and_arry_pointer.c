/*
 * 数组指针:
 * 当指针变量里存放一个数组的首地址时，此指针变量称为指向数组的指针变量，简称`数组指针`。
 * 例如
 *   整形指针 int *p;
 *   浮点型指针 float *p;
 *
 * 指针数组:
 * 数组是用来存放一系列相同类型的数据，
 * 当然数组也可以用来存放指针，这种用来`存放指针的数组`被称为指针数组，它要求存放在数组中指针的`数据类型必须一致`。
 *指针数组-->是存放指针的数组
 * 格式 ：数据类型 *指针数组名[大小];
 * 举例 :
 *    int *arr[5];  <--这样就定义了一个指针数组，每个元素都是一个整形指针即 int *
 * */

